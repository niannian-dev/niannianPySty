#定义变量
lunk_number = 8

#打印变量类型
print('lunk_number =',type(lunk_number))

#变量的数据类型可以修改
lunk_number = "北京欢迎你"

#打印变量类型
print('lunk_number =',type(lunk_number))

#打印变量值
print('lunk_number =',lunk_number)

#多个变量可以指向一个值
a = b = c = 100
print(a)
print(b)
print(c)
print(id(a))
print(id(b))
print(id(c))

